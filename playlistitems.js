// 創建YouTube播放器
function createPlayer() {
    if (typeof YT !== 'undefined' && YT.Player) {
        player = new YT.Player('youtube-player', {
            height: '0',
            width: '0',
            videoId: playlistItems[0].snippet.resourceId.videoId,
            events: {
                'onReady': onPlayerReady,
                'onStateChange': onPlayerStateChange
            },
            playerVars: {
                'controls': 0,
                'showinfo': 0,
                'rel': 0
            }
        });
    }
}

// 播放器就緒時調用
function onPlayerReady(event) {
    // 設置初始音量
    player.setVolume(70);
    
    // 定時更新進度條
    setInterval(updateProgressBar, 1000);
    
    // 更新歌曲信息
    updateSongInfo(0);
    highlightCurrentSong();
}

// 播放器狀態改變時調用
function onPlayerStateChange(event) {
    // YT.PlayerState.PLAYING = 1
    if (event.data === YT.PlayerState.PLAYING) {
        isPlaying = true;
        updatePlayPauseButton();
    } 
    // YT.PlayerState.PAUSED = 2
    else if (event.data === YT.PlayerState.PAUSED) {
        isPlaying = false;
        updatePlayPauseButton();
    } 
    // YT.PlayerState.ENDED = 0
    else if (event.data === YT.PlayerState.ENDED) {
        isPlaying = false;
        updatePlayPauseButton();
        
        // 檢查播放模式
        if (isSingleLoop) {
            // 單曲循環，重新播放當前歌曲
            player.playVideo();
        } else if (isLooping && currentVideoIndex === playlistItems.length - 1) {
            // 循環播放且為最後一首，播放第一首
            loadVideo(0);
        } else {
            // 播放下一首
            playNextSong();
        }
    }
}

// 初始化播放清單UI
function initializePlaylist() {
    const playlistElement = document.getElementById('playlist');
    playlistElement.innerHTML = '';
    
    playlistItems.forEach((item, index) => {
        const listItem = document.createElement('div');
        listItem.className = 'playlist-item flex items-center p-2 hover:bg-gray-100 dark:hover:bg-gray-700 rounded cursor-pointer';
        listItem.dataset.index = index;
        
        // 縮略圖
        const thumbnail = document.createElement('img');
        thumbnail.src = item.snippet.thumbnails.default.url;
        thumbnail.className = 'w-10 h-10 object-cover rounded mr-3';
        
        // 歌曲信息
        const infoDiv = document.createElement('div');
        infoDiv.className = 'flex-1 overflow-hidden';
        
        const title = document.createElement('div');
        title.className = 'text-sm font-medium text-gray-800 dark:text-gray-200 truncate';
        title.textContent = item.snippet.title;
        
        const channel = document.createElement('div');
        channel.className = 'text-xs text-gray-500 dark:text-gray-400 truncate';
        channel.textContent = item.snippet.channelTitle;
        
        infoDiv.appendChild(title);
        infoDiv.appendChild(channel);
        
        // 組合元素
        listItem.appendChild(thumbnail);
        listItem.appendChild(infoDiv);
        
        // 點擊播放
        listItem.addEventListener('click', function() {
            const index = parseInt(this.dataset.index);
            loadVideo(index);
        });
        
        playlistElement.appendChild(listItem);
    });
}

// 載入和播放視頻
function loadVideo(index) {
    if (index < 0 || index >= playlistItems.length) return;
    
    currentVideoIndex = index;
    const videoId = playlistItems[index].snippet.resourceId.videoId;
    
    if (player && player.loadVideoById) {
        player.loadVideoById(videoId);
        updateSongInfo(index);
        highlightCurrentSong();
    }
}

// 更新歌曲信息
function updateSongInfo(index) {
    if (index < 0 || index >= playlistItems.length) return;
    
    const item = playlistItems[index];
    document.getElementById('song-title').textContent = item.snippet.title;
    document.getElementById('channel-title').textContent = item.snippet.channelTitle;
    
    // 使用最高質量的縮略圖
    let thumbnailUrl = '';
    if (item.snippet.thumbnails.maxres) {
        thumbnailUrl = item.snippet.thumbnails.maxres.url;
    } else if (item.snippet.thumbnails.standard) {
        thumbnailUrl = item.snippet.thumbnails.standard.url;
    } else if (item.snippet.thumbnails.high) {
        thumbnailUrl = item.snippet.thumbnails.high.url;
    } else {
        thumbnailUrl = item.snippet.thumbnails.default.url;
    }
    
    document.getElementById('song-thumbnail').src = thumbnailUrl;
}

// 更新播放/暫停按鈕
function updatePlayPauseButton() {
    const button = document.getElementById('play-pause');
    if (isPlaying) {
        button.innerHTML = '<img width="35" height="35" src="https://img.icons8.com/nolan/25/pause.png" alt="pause"/>';
    } else {
        button.innerHTML = '<img width="35" height="35" src="https://img.icons8.com/nolan/25/play.png" alt="play"/>';
    }
}

// 更新進度條
function updateProgressBar() {
    if (!player || !player.getCurrentTime) return;
    
    try {
        const currentTime = player.getCurrentTime() || 0;
        const duration = player.getDuration() || 0;
        
        if (duration > 0) {
            // 更新進度條
            const percent = (currentTime / duration) * 100;
            document.getElementById('progress-fill').style.width = `${percent}%`;
            
            // 更新時間顯示
            document.getElementById('current-time').textContent = formatTime(currentTime);
            document.getElementById('duration').textContent = formatTime(duration);
        }
    } catch (e) {
        console.error('更新進度條時出錯:', e);
    }
}

// 格式化時間為 mm:ss
function formatTime(seconds) {
    const minutes = Math.floor(seconds / 60);
    seconds = Math.floor(seconds % 60);
    return `${minutes}:${seconds < 10 ? '0' : ''}${seconds}`;
}

// 播放下一首歌
function playNextSong() {
    let nextIndex;
    
    if (isShuffling) {
        // 隨機播放 - 從剩餘歌曲中隨機選擇
        nextIndex = Math.floor(Math.random() * playlistItems.length);
        
        // 避免連續播放同一首
        while (nextIndex === currentVideoIndex && playlistItems.length > 1) {
            nextIndex = Math.floor(Math.random() * playlistItems.length);
        }
    } else {
        // 順序播放 - 下一首或回到第一首
        nextIndex = currentVideoIndex + 1;
        if (nextIndex >= playlistItems.length) {
            nextIndex = isLooping ? 0 : currentVideoIndex;
        }
    }
    
    loadVideo(nextIndex);
}

// 播放上一首歌
function playPreviousSong() {
    let prevIndex;
    
    if (isShuffling) {
        // 隨機播放模式下的上一首也是隨機選擇
        prevIndex = Math.floor(Math.random() * playlistItems.length);
        while (prevIndex === currentVideoIndex && playlistItems.length > 1) {
            prevIndex = Math.floor(Math.random() * playlistItems.length);
        }
    } else {
        // 順序播放 - 上一首或到最後一首
        prevIndex = currentVideoIndex - 1;
        if (prevIndex < 0) {
            prevIndex = isLooping ? playlistItems.length - 1 : 0;
        }
    }
    
    loadVideo(prevIndex);
}

// 高亮當前播放的歌曲
function highlightCurrentSong() {
    // 移除所有歌曲的高亮
    document.querySelectorAll('.playlist-item').forEach(item => {
        item.classList.remove('bg-gray-100', 'dark:bg-gray-700', 'border-l-4', 'border-indigo-500');
    });
    
    // 高亮當前播放的歌曲
    const currentItem = document.querySelector(`.playlist-item[data-index="${currentVideoIndex}"]`);
    if (currentItem) {
        currentItem.classList.add('bg-gray-100', 'dark:bg-gray-700', 'border-l-4', 'border-indigo-500');
        
        // 確保當前播放的歌曲可見
        currentItem.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
    }
}

